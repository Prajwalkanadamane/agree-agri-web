export default function SoilTestLabs() {
  const [selectedState, setSelectedState] = useState('');
  const [selectedDistrict, setSelectedDistrict] = useState('');
  
  const [mapQuery, setMapQuery] = useState('');
  const [isLocating, setIsLocating] = useState(false);
  const [error, setError] = useState('');

  // Auto-update map when manual dropdowns are changed
  useEffect(() => {
    if (selectedState && selectedDistrict) {
      setError('');
      // Standardized Google Maps query string
      const query = encodeURIComponent(`Soil Testing Laboratory in ${selectedDistrict}, ${selectedState}, India`);
      setMapQuery(query);
    }
  }, [selectedState, selectedDistrict]);

  const handleLiveLocation = () => {
    setIsLocating(true);
    setError('');
    
    if (!navigator.geolocation) {
      setError('Geolocation is not supported by your browser.');
      setIsLocating(false);
      return;
    }

    navigator.geolocation.getCurrentPosition(
      (position) => {
        const lat = position.coords.latitude;
        const lon = position.coords.longitude;
        const query = encodeURIComponent(`Soil Testing Laboratory near ${lat},${lon}`);
        setMapQuery(query);
        
        // Clear dropdowns since we are using live GPS coordinates
        setSelectedState('');
        setSelectedDistrict('');
        setIsLocating(false);
      },
      (err) => {
        setError('Unable to retrieve live location. Please check browser permissions or use the manual search.');
        setIsLocating(false);
      }
    );
  };

  return (
    <div className="bg-white p-6 rounded-2xl shadow-sm border border-gray-100 mb-8">
      <div className="flex flex-col sm:flex-row justify-between items-start sm:items-center gap-4 mb-6">
        <div className="flex items-center gap-3">
          <FiMap className="text-3xl text-green-600" />
          <div>
            <h3 className="text-xl font-bold text-gray-900">Find Soil Test Labs</h3>
            <p className="text-sm text-gray-500">Locate government and private soil testing facilities near you.</p>
          </div>
        </div>
        
        <button 
          onClick={handleLiveLocation} 
          disabled={isLocating}
          className="bg-green-50 text-green-700 border border-green-200 hover:bg-green-100 font-bold py-2 px-4 rounded-lg transition-colors flex items-center gap-2 text-sm disabled:opacity-50"
        >
          {isLocating ? (
            <><FiRadio className="animate-pulse" /> Locating...</>
          ) : (
            <><FiMapPin /> Use Live Location</>
          )}
        </button>
      </div>

      <div className="flex flex-col space-y-6">
        {/* --- Smart Location Dropdowns --- */}
        <div className="flex flex-col sm:flex-row gap-4">
          <select 
            value={selectedState} 
            onChange={(e) => {
              setSelectedState(e.target.value);
              setSelectedDistrict(''); 
            }}
            className="flex-1 border border-gray-300 rounded-lg p-3 focus:ring-2 focus:ring-green-500 outline-none bg-white text-gray-800 font-medium cursor-pointer"
          >
            <option value="">Select State...</option>
            {Object.keys(IndianStatesAndDistricts).sort().map(state => (
              <option key={state} value={state}>{state}</option>
            ))}
          </select>

          <select 
            value={selectedDistrict} 
            onChange={(e) => setSelectedDistrict(e.target.value)}
            disabled={!selectedState}
            className="flex-1 border border-gray-300 rounded-lg p-3 focus:ring-2 focus:ring-green-500 outline-none bg-white text-gray-800 font-medium cursor-pointer disabled:opacity-50 disabled:bg-gray-100"
          >
            <option value="">Select District...</option>
            {selectedState && IndianStatesAndDistricts[selectedState].map(district => (
              <option key={district} value={district}>{district}</option>
            ))}
          </select>
        </div>

        {error && (
          <div className="p-3 bg-red-50 text-red-700 border border-red-200 rounded-lg text-sm flex items-center gap-2">
            <FiAlertTriangle className="shrink-0" /> {error}
          </div>
        )}

        {/* --- Map Display --- */}
        <div className="w-full h-80 bg-gray-50 rounded-xl border-2 border-dashed border-gray-300 overflow-hidden relative flex items-center justify-center">
          {!mapQuery && !isLocating && (
             <div className="text-center text-gray-400 p-6 flex flex-col items-center">
               <FiSearch className="text-4xl mb-3 text-gray-300" />
               <p>Select your state and district or use live location to see nearby labs.</p>
             </div>
          )}

          {isLocating && (
             <div className="text-center text-green-600 flex flex-col items-center p-6">
               <FiRadio className="text-4xl mb-3 animate-pulse" />
               <p className="font-bold">Requesting GPS coordinates...</p>
             </div>
          )}

          {mapQuery && !isLocating && (
            <iframe
              width="100%"
              height="100%"
              frameBorder="0"
              style={{ border: 0 }}
              src={`https://maps.google.com/maps?q=${mapQuery}&t=&z=11&ie=UTF8&iwloc=&output=embed`}
              allowFullScreen
              title="Soil Labs Map"
              className="animate-fade-in-up"
            ></iframe>
          )}
        </div>
      </div>
    </div>
  );
}